<script>
    $(window).on('load', function(){
        // Preloader
        $('.loader').fadeOut();
        $('.loader-mask').delay(350).fadeOut('slow');
    });

    function loading (){
        document.getElementById("spin").style.display = "flex";
        document.getElementById("submit").style.display = "none";
    }
    function loadingComplete (){
        document.getElementById("spin").style.display = "none";
        document.getElementById("submit").style.display = "block";
    }

    $(document).ready(async  function(){
        let name = await localStorage.getItem("name");
        if(name === null) {
            loadname();
        }else{
            loadtopics();
        }

    });

    function loadtopics(){
        $("#loaddata").text("");
        $("#loaddata").load("topics.php",
            function(response, status, xhr) {
                if (status == "success") {
                    // Code to execute after the load is complete
                    console.log("Load completed successfully");
                } else {
                    // Code to handle errors
                    console.log("Error loading data");
                }
            });
    }
    function loadname(){
        $("#loaddata").text("");
        $("#loaddata").load("name.php",
            function(response, status, xhr) {
                if (status == "success") {
                    // Code to execute after the load is complete
                    console.log("Load completed successfully");
                    $("#myform").submit(e=>{
                        loading();
                        e.preventDefault();
                        let name = $("#name").val();
                        if(name !==""){
                            const data = new FormData();
                            data.append("name",name);
                            $.ajax({
                                url:"https://hayahafrica.com/admin/apis/save_name.php",
                                data: data,
                                method: 'POST',
                                type: 'POST',
                                dataType: 'json',
                                processData: false,
                                contentType: false,
                                success:async (resp)=>{
                                    if(resp.status === 1){
                                        let dt = JSON.stringify(resp.data);
                                        await localStorage.setItem("name",dt);
                                        console.log(dt);
                                        loadingComplete();
                                        loadtopics();
                                    }else{
                                        alert("user is already taken");
                                        loadingComplete();
                                    }
                                },
                                finally:()=>{
                                    loadingComplete();
                                }
                            });
                        }
                    })
                } else {
                    // Code to handle errors
                    console.log("Error loading data");
                }
            });
    }
    function loadquestions(){
        $("#loaddata").text("");
        let topic = localStorage.getItem('topic');
        $("#loaddata").load("questions.php?topic="+topic,
            function(response, status, xhr) {
                if (status == "success") {
                    // Code to execute after the load is complete
                    console.log("Load completed successfully");
                } else {
                    // Code to handle errors
                    console.log("Error loading data");
                }
            });
    }

</script>
